"""Geo is a module of geometric functions."""


def square(n):
    """Ginds the square of a value."""
    return n*n


def triangle(n):
    """Finds triangular number based on size of side."""
    return n * (n + 1) / 2


def cube(n):
    """Finds cube of number."""
    return n**3
